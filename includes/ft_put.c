#include "ft_printf.h"

void	ft_putchar(char c)  // UNICODE.
{
	t_r.r++;
	write(1, &c, 1);
}

void	ft_putstr(char *s)
{
	int	i;

	i = -1;
	while (s[++i])
		ft_putchar(s[i]);
}
